def _resolve_principal(session: Session, role_code: str, label: str) -> str:
    rows = session.execute(
        select(AppUser.id)
        .join(UserRole, UserRole.user_id == AppUser.id)
        .join(Role, Role.id == UserRole.role_id)
        .where(AppUser.tenant_id == DEMO_TENANT_ID, Role.code == role_code)
    ).all()
    if len(rows) != 1:
        raise DemoRs1PrereqError(
            f"the demo tenant holds {len(rows)} {label} principal(s) — expected exactly one "
            f"from the MG-1 campaign; run it first"
        )
    return str(rows[0][0])


def _resolve_version(session: Session, code: str, code_version: str) -> ModelVersion:
    versions = (
        session.execute(
            select(ModelVersion)
            .join(Model, Model.id == ModelVersion.model_id)
            .where(
                Model.tenant_id == DEMO_TENANT_ID,
                Model.code == code,
                ModelVersion.code_version == code_version,
            )
        )
        .scalars()
        .all()
    )
    if len(versions) != 1:
        raise DemoRs1PrereqError(
            f"the demo tenant holds {len(versions)} {code_version!r} version(s) of {code!r} — "
            f"expected exactly one (a prior stage unseeded or the tenant drifted); refusing"
        )
    return versions[0]


