def _dt(on: date) -> datetime:
    return datetime(on.year, on.month, on.day, 12, 0, tzinfo=UTC)


def _cycle_value(cycle: tuple[str, ...], on: date) -> Decimal:
    return Decimal(cycle[(on - _MF_RETURN_START).days % len(cycle)])


def _eqc_mark_series() -> tuple[Decimal, ...]:
    """MF-EQ-C's deterministic marks: mark_i = mark_{i-1}*(1 + L_MKT*r_MKT(d_i) + eps_i), 6dp."""
    marks = [Decimal(_EQC_START_MARK)]
    for i, on in enumerate(_MF_MARK_DATES[1:]):
        r = Decimal(_EQC_MARKET_LOADING) * _cycle_value(_MKT_CYCLE, on) + Decimal(
            _EQC_EPS[i % len(_EQC_EPS)]
        )
        marks.append((marks[-1] * (Decimal("1") + r)).quantize(_SIX_DP, rounding=ROUND_HALF_UP))
    return tuple(marks)


class DemoRs1Error(RuntimeError):
    """Stage 5 could not complete against the live tenant state (fail-loud)."""


class DemoRs1PrereqError(DemoRs1Error):
    """Stage 5 extends the LIVING tenant — it requires the MG-1 campaign AND the MF-1 sleeve
    (the raw estimates it transforms) and never bootstraps them. Run the prior stages first."""


class DemoRs1AlreadySeededError(RuntimeError):
    """Refuse-not-skip on stage 5's OWN footprint: the EWMA estimator version already exists.
    Append-only records are never double-filed; reset the schema and re-run all five stages."""

    def __init__(self) -> None:
        super().__init__(
            f"demo tenant {DEMO_TENANT_ID} already holds an EWMA estimator version of "
            f"{PROXY_WEIGHT_MODEL_CODE!r} — refusing to re-run stage 5 (reset the schema and "
            f"re-run the full five-stage sequence)."
        )


